// **Iteración #1: Buscar el máximo**
// Completa la función que tomando dos números como argumento devuelva el más alto.
let num1 = 1;
let num2 = 2;

function sum(num1, num2) {
  if (num1 > num2) {
    console.log("El numero mayor es: " + num1);
  } else {
    console.log("El numero mayor es: " + num2);
  }
}

sum(num1, num2);
